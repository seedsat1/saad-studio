function SortableBlock({
  block,
  isSelected,
  onSelect,
}: {
  block: LayoutBlock;
  isSelected: boolean;
  onSelect: () => void;
}) {
  const { attributes, listeners, setNodeRef, transform, transition, isDragging } =
    useSortable({ id: block.id });

  const style: React.CSSProperties = {
    transform: CSS.Transform.toString(transform),
    transition,
    opacity: isDragging ? 0.35 : 1,
    zIndex: isDragging ? 0 : undefined,
  };

  return (
    <div ref={setNodeRef} style={style} className="group relative">
      {/* Selection ring */}
      <div
        onClick={onSelect}
        className={`cursor-pointer rounded-xl transition-all duration-200 ${
          isSelected
            ? "ring-2 ring-violet-500 ring-offset-2 ring-offset-slate-950"
            : "ring-1 ring-transparent hover:ring-slate-600"
        }`}
      >
        <BlockPreview block={block} />
      </div>

      {/* Drag handle */}
      <div
        {...attributes}
        {...listeners}
        className="absolute left-2 top-1/2 -translate-y-1/2 opacity-0 group-hover:opacity-100 transition-opacity cursor-grab active:cursor-grabbing z-20"
        onClick={(e) => e.stopPropagation()}
      >
        <div className="bg-slate-900/90 rounded-md p-1 border border-slate-700 shadow-lg">
          <GripVertical className="w-4 h-4 text-slate-400" />
        </div>
      </div>

      {/* Selected indicator */}
      {isSelected && (
        <div className="absolute top-2 left-2 bg-violet-600 text-white text-[9px] px-2 py-0.5 rounded-full font-bold z-20 pointer-events-none">
          SELECTED
        </div>
      )}
    </div>
  );
}

